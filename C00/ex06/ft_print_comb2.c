#include <unistd.h>

void	ft_print_comb2(void)
{
	char	a;
	char	b;

	a = '0';
	b = '0';
	while (a <= '9')
	{
		write(1, &a, 1);
		write(1, &b, 1);
		if (b == '9')
		{
			a++;
			b = '0';
		}
		if (a != 9 || b != 9)
			write(1, ", ", 2);
		b++;
	}
}

int	main(void)
{
	ft_print_comb2();
}